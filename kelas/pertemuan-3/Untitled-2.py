# contoh penggunaan fungsi bawaan untuk menupulasi string
text = "hello, fachry apa kabar!"
print(len(text))  # menghitung panjang string
print(text.upper())  # mengubah string menjadi huruf besar
print(text.replace("fachry", "Fachry"))  # mengganti kata "fachry" dengan "Fachry"

# contoh pengguanaan fungsi bawaaan untai minimum dalam listuk operasi matematika
number = [3, 100, 4, 1, 50, 9, 2000, 6, 5]
print(sum(number))  # menjumlahkan semua elemen dalam list
print(max(number))  # mencari nilai maksimum dalam list 
print(min(number))  # mencari nilai minimum dalam list  
print(sorted(number))  # mengurutkan elemen dalam list secara ascending