# program menggunakan fungsi bawaan

angka = -7.856
daftar = [5, 2, 9, 1, 7]
string = "orang baik itu sulit ditemukan"

print(round(angka))   # Untuk membulatkan angka ke bilangan bulat terdekat
print(abs(angka))     # Untuk mendapatkan nilai absolut dari angka
print(sorted(daftar)) # Untuk mengurutkan elemen dalam daftar
print(type(angka))    # Untuk mengetahui tipe data dari variabel angka
print(len(daftar))   # Untuk menghitung jumlah elemen dalam daftar
print(len(string))    # Untuk menghitung jumlah karakter dalam string "orang baik itu sulit ditemukan"