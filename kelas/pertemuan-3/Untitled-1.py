# Program: Contoh Fungsi Python dan Penjelasannya

print("Contoh Fungsi Python dan Penjelasannya")
print("1. round(x) → membulatkan angka")
print("   Contoh: round(4.6) =", round(4.6))

print("2. abs(x) → nilai absolut")
print("   Contoh: abs(-8) =", abs(-8))

print("3. sorted(list) → mengurutkan data")
print("   Contoh: sorted([5,2,3]) =", sorted([5, 2, 3]))

print("4. type(x) → mengetahui tipe data")
print("   Contoh: type(3.14) =", type(3.14))

print("5. len(x) → menghitung jumlah elemen")
print("   Contoh: len('Halo') =", len('Halo'))